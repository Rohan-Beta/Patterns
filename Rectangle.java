import java.util.*;

class Firstclass {
    public static void main(String args[]) {
        for(int i = 0; i < 4; i += 1) { // i used for rows
            for(int j = 0; j < 5; j += 1) {// j used for columns
                System.out.print("*");
            }
            System.out.print("\n");
        }
    }
}
